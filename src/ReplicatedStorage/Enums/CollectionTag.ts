/**
 * Typed `CollectionTag` tags.
 */
export const enum CollectionTag {
	/**
	 * Generic ore.
	 */
	ORE = "ORE",
	/**
	 * Generic item.
	 */
	ITEM = "ITEM",
	/**
	 * A player instance.
	 */
	USER = "USER",
	/**
	 * A conveyor.
	 */
	CONVEYOR = "Conveyor",
	/**
	 * A dropper.
	 */
	DROPPER = "Dropper",
	/**
	 * A furnace.
	 */
	FURNACE = "Furnace",
	/**
	 * A upgrader.
	 */
	UPGRADER = "Upgrader",
}
